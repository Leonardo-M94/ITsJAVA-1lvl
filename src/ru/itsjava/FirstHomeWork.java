package ru.itsjava;

public class FirstHomeWork {
    public static void main(String[] args) {
        System.out.println("Я люблю домашние задания!");

        int num1 = 10;
        int num2 = 7;

        System.out.println(num1 + " + " + num2 + " = " + (num1 + num2));
        System.out.println(num1 + " - " + num2 + " = " + (num1 - num2));
        System.out.println(num1 + " * " + num2 + " = " + (num1 * num2));
        System.out.println(num1 + " / " + num2 + " = " + (num1 / num2));
        System.out.println(num1 + " % " + num2 + " = " + (num1 % num2));

        System.out.println(num1 + "++ " + "= " + (++num1));
        System.out.println(num2 + "++ " + "= " + (++num2));
        System.out.println(num1 + "-- " + "= " + (--num1));
        System.out.println(num2 + "-- " + "= " + (--num2));

        System.out.println(num1 + " > " + num2 + " -> " + (num1 > num2));
        System.out.println(num1 + " < " + num2 + " -> " + (num1 < num2));
        System.out.println(num1 + " >= " + num2 + " -> " + (num1 >= num2));
        System.out.println(num1 + " <= " + num2 + " -> " + (num1 <= num2));
        System.out.println(num1 + " == " + num2 + " -> " + (num1 == num2));
        System.out.println(num1 + " != " + num2 + " -> " + (num1 != num2));
    }
}
